export type RestaurantsQueryVariables = Exact<{ [key: string]: never; }>;


export type RestaurantsQuery = { __typename?: 'Query', restaurants?: { __typename?: 'RestaurantEntityResponseCollection', data: Array<{ __typename?: 'RestaurantEntity', id?: string | null, attributes?: { __typename?: 'Restaurant', name: string, description?: string | null } | null }> } | null };


export const RestaurantsDocument = `
    query Restaurants {
  restaurants {
    data {
      id
      attributes {
        name
        description
      }
    }
  }
}
    `;
export const useRestaurantsQuery = <
      TData = RestaurantsQuery,
      TError = unknown
    >(
      client: GraphQLClient,
      variables?: RestaurantsQueryVariables,
      options?: UseQueryOptions<RestaurantsQuery, TError, TData>,
      headers?: RequestInit['headers']
    ) =>
    useQuery<RestaurantsQuery, TError, TData>(
      variables === undefined ? ['Restaurants'] : ['Restaurants', variables],
      fetcher<RestaurantsQuery, RestaurantsQueryVariables>(client, RestaurantsDocument, variables, headers),
      options
    );

useRestaurantsQuery.getKey = (variables?: RestaurantsQueryVariables) => variables === undefined ? ['Restaurants'] : ['Restaurants', variables];
;
